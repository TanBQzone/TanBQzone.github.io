// 获取随机线路
const getRandomLine = (info) => {
    const lines = Object.keys(info);
    const randomIndex = Math.floor(Math.random() * lines.length);
    return lines[randomIndex];
};

// 获取随机站点
const getRandomStation = (info, line) => {
    const stations = info[line];
    const randomIndex = Math.floor(Math.random() * stations.length);
    return stations[randomIndex];
};

const btnLine = document.getElementById('randomLine');
const lineInfo = document.querySelector('.line');

const btnStation = document.getElementById('randomStation');
const stationInfoCN = document.querySelector('.station-cn');
const stationInfoEN = document.querySelector('.station-en');

let randomLine = '';

btnLine.addEventListener('click', () => {
    randomLine = getRandomLine(MetroInfo);
    lineInfo.textContent = randomLine;
    stationInfoCN.textContent = '';
    stationInfoEN.textContent = '';
    console.log("getRandomLine succeed!");
});

btnStation.addEventListener('click', () => {
    if (randomLine) {
        const randomStation = getRandomStation(MetroInfo, randomLine);
        stationInfoCN.textContent = randomStation['中文站名'];
        stationInfoEN.textContent = randomStation['英文站名'];
        console.log("getRandomStation succeed!");
    } else {
        stationInfoEN.textContent = '请生成随机线路';
        console.log("getRandomStation failed! Please generate random lines first!");
    }
});